#include <unistd.h>
#include <stdio.h>
#include <string.h>
#include "libft.h"
/** @brief Applies a function to each element's content
 *  @param lst - list
 * 	@param f - function to apply
*/
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!f)
		return ;
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
